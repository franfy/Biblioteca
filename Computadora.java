package Biblioteca;

public class Computadora {

	int nro;
	String estado;

	public Computadora(int nro, String estado) {
		super();
		this.nro = nro;
		this.estado = estado;
	}

	public int getNro() {
		return nro;
	}

	public void setNro(int nro) {
		this.nro = nro;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}
	
	
	
}